package hotel;

public abstract class Located extends Unique {
    
    // The Located class defines the abstract features of an
    // element with a name and address...

    private String name;

    private String address;

    public Located(String name, String address) {
	super();
	this.name = name;
	this.address = address;
    }

    public String getAddress() {
	return address;
    }

    public String getName() {
	return name;
    }

    public void setAddress(String address) {
	this.address = address;
    }

    public void setName(String name) {
	this.name = name;
    }

}
